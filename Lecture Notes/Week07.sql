-- ER diagrams: entity-relationship diagrams

-- Entity types: nouns 
-- relationships: describe connections between nouns

-- Unary relationship: 1 entity has a relationship with itself
-- Binary relationship: 2 entities have a relationship
-- Ternary relationship: multiple entities with a relationship

-- Associative entity: relationships can have attributes
    -- these describe features pertaining to the association between entities in the relationship

-- Associative entity - combination of relationship and entity
    -- many attributes on a relationship suugest use of an entity instead of a relationship
    -- has to be many-to-many
